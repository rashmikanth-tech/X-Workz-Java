public class Neptune {
    public static void neptuneDefault(String defaultColor) {
        System.out.println("Neptune Default Color: " + defaultColor);
    }

    public static void stormColor(String stormColor) {
        System.out.println("Neptune Storm Color: " + stormColor);
    }

    public static void atmosphereColor(String atmosphereColor) {
        System.out.println("Neptune Atmosphere Color: " + atmosphereColor);
    }

    public static void gravity(double gravity, short temperature) {
        System.out.println("Neptune Gravity: " + gravity + ", Temperature: " + temperature);
    }

    public static void temperature(short temperature) {
        System.out.println("Neptune Temperature: " + temperature);
    }

    public static void diameter(int diameter) {
        System.out.println("Neptune Diameter: " + diameter);
    }

    public static void distanceFromSun(long distance) {
        System.out.println("Neptune Distance from Sun: " + distance);
    }
}
