public class Mars {
    public static void marsDefault(String defaultColor) {
        System.out.println("Mars Default Color: " + defaultColor);
    }

    public static void surfaceColor(String surfaceColor) {
        System.out.println("Mars Surface Color: " + surfaceColor);
    }

    public static void skyColor(String skyColor) {
        System.out.println("Mars Sky Color: " + skyColor);
    }

    public static void gravity(double gravity, byte temperature) {
        System.out.println("Mars Gravity: " + gravity + ", Temperature: " + temperature);
    }

    public static void temperature(byte temperature) {
        System.out.println("Mars Temperature: " + temperature);
    }

    public static void diameter(int diameter) {
        System.out.println("Mars Diameter: " + diameter);
    }

    public static void distanceFromSun(long distance) {
        System.out.println("Mars Distance from Sun: " + distance);
    }
}
