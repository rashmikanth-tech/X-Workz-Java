public class Venus {
    public static void venusDefault(String defaultColor) {
        System.out.println("Venus Default Color: " + defaultColor);
    }

    public static void atmosphereColor(String atmosphereColor) {
        System.out.println("Venus Atmosphere Color: " + atmosphereColor);
    }

    public static void skyColor(String skyColor) {
        System.out.println("Venus Sky Color: " + skyColor);
    }

    public static void gravity(double gravity, int temperature) {
        System.out.println("Venus Gravity: " + gravity + ", Temperature: " + temperature);
    }

    public static void temperature(int temperature) {
        System.out.println("Venus Temperature: " + temperature);
    }

    public static void diameter(int diameter) {
        System.out.println("Venus Diameter: " + diameter);
    }

    public static void distanceFromSun(long distance) {
        System.out.println("Venus Distance from Sun: " + distance);
    }
}
