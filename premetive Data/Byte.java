class Byte
{
	public static void main(String[] args)
	{
		byte age=10;
		byte pravith=20;
		byte skanda=30;
		byte keys=50;
		byte car=15;
		System.out.println("Initinal Values;");
		System.out.println("Age: "+age);
		System.out.println("Pravith: "+pravith);
		System.out.println("Skanda: "+skanda);
		System.out.println("keys: "+keys);
		System.out.println("car: "+car);
		
		age=5;
		pravith=15;
		skanda=25;
		keys=45;
		car=4;
		System.out.println("Re-Initinal values: ");
		System.out.println("Age: "+age);
		System.out.println("Pravith: "+pravith);
		System.out.println("Skanda: "+skanda);
		System.out.println("Keys: "+keys);
		System.out.println("Car: "+car);
	}
}