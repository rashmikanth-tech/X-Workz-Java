public class Uranus {
    public static void uranusDefault(String defaultColor) {
        System.out.println("Uranus Default Color: " + defaultColor);
    }

    public static void atmosphereColor(String atmosphereColor) {
        System.out.println("Uranus Atmosphere Color: " + atmosphereColor);
    }

    public static void skyColor(String skyColor) {
        System.out.println("Uranus Sky Color: " + skyColor);
    }

    public static void gravity(double gravity, short temperature) {
        System.out.println("Uranus Gravity: " + gravity + ", Temperature: " + temperature);
    }

    public static void temperature(short temperature) {
        System.out.println("Uranus Temperature: " + temperature);
    }

    public static void diameter(int diameter) {
        System.out.println("Uranus Diameter: " + diameter);
    }

    public static void distanceFromSun(long distance) {
        System.out.println("Uranus Distance from Sun: " + distance);
    }
}
