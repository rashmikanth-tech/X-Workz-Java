public class Jupiter {
    public static void jupiterDefault(String defaultColor) {
        System.out.println("Jupiter Default Color: " + defaultColor);
    }

    public static void stormColor(String stormColor) {
        System.out.println("Jupiter Storm Color: " + stormColor);
    }

    public static void atmosphereColor(String atmosphereColor) {
        System.out.println("Jupiter Atmosphere Color: " + atmosphereColor);
    }

    public static void gravity(double gravity, short temperature) {
        System.out.println("Jupiter Gravity: " + gravity + ", Temperature: " + temperature);
    }

    public static void temperature(short temperature) {
        System.out.println("Jupiter Temperature: " + temperature);
    }

    public static void diameter(int diameter) {
        System.out.println("Jupiter Diameter: " + diameter);
    }

    public static void distanceFromSun(long distance) {
        System.out.println("Jupiter Distance from Sun: " + distance);
    }
}
