public class Saturn {
    public static void saturnDefault(String defaultColor) {
        System.out.println("Saturn Default Color: " + defaultColor);
    }

    public static void ringColor(String ringColor) {
        System.out.println("Saturn Ring Color: " + ringColor);
    }

    public static void atmosphereColor(String atmosphereColor) {
        System.out.println("Saturn Atmosphere Color: " + atmosphereColor);
    }

    public static void gravity(double gravity, short temperature) {
        System.out.println("Saturn Gravity: " + gravity + ", Temperature: " + temperature);
    }

    public static void temperature(short temperature) {
        System.out.println("Saturn Temperature: " + temperature);
    }

    public static void diameter(int diameter) {
        System.out.println("Saturn Diameter: " + diameter);
    }

    public static void distanceFromSun(long distance) {
        System.out.println("Saturn Distance from Sun: " + distance);
    }
}
