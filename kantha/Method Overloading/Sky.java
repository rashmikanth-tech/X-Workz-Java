public class Sky {
    public static void moonDefault(String defaultColor) {
        System.out.println("Moon Default Color: " + defaultColor);
    }

    public static void fullMoon(String fullMoon) {
        System.out.println("Full Moon Color: " + fullMoon);
    }

    public static void nearHorizon(String nearHorizon) {
        System.out.println("Color near Horizon: " + nearHorizon);
    }

    public static void gravity(double gravity, byte temperature) {
        System.out.println("Moon Gravity: " + gravity);
    }

    public static void temperature(byte temperature) {
        System.out.println("Moon Temperature: " + temperature);
    }

    public static void diameter(int diameter) {
        System.out.println("Moon Diameter: " + diameter);
    }

    public static void outside(long distance) {
        System.out.println("Moon Distance from Earth: " + distance);
    }
}
